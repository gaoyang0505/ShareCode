    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Disease disease = new Disease();
        disease.setNameDisease(jComboBox2.getSelectedItem().toString());
        UserAccount userAcco = (UserAccount) jComboBox3.getSelectedItem();
        ((ResidenceRole) userAcco.getRole()).getDiseaseDirectory().addDisease(disease);
        populateDisTable();
    }//GEN-LAST:event_jButton1ActionPerformed

    public void populateDisTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
//(Business.Role.ResidenceRole) Business.Role.ResidenceRole
        for (Organization or : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (or instanceof ResidenceOrganization) {
                for (UserAccount usa : or.getUserAccountDirectory().getUserAccountList()) {
                    if (usa.getRole().toString().equals("Business.Role.ResidenceRole")) {
                        Object row[] = new Object[2];
                        row[0] = usa.getUsername();
                        for(Disease si: ((ResidenceRole)usa.getRole()).getDiseaseDirectory().getDiseaseList())
                            row[1] = si.getNameDisease();
                        ((DefaultTableModel) jTable1.getModel()).addRow(row);
                    }
                }
            }
            
        }
    }

