    private void jSendToAllBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSendToAllBtnActionPerformed
        String message = jTextArea1.getText();
        MedicalWorkRequest request = new MedicalWorkRequest();
        request.setMessage(message);
        request.setMessageType(jComboBox1.getSelectedItem().toString());
        request.setSender(userAccount);
        request.setStatus("Sent");

        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (organization instanceof ResidenceOrganization) {
                org = organization;
                break;
            }
        }
        if (org != null) {
            org.getWorkQueue().getWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);
            JOptionPane.showMessageDialog(null, "Sent Successfully!");
        }
    }//GEN-LAST:event_jSendToAllBtnActionPerformed

